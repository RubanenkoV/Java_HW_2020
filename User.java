package com.company;

public class User {
    public String name;
    public String surname;
    public int age;
    public int number_of_accounts;

    public User(String name, String surname, int age, int number_of_accounts){
        this.name = name;
        this.surname = surname;
        this.age = age;
        this.number_of_accounts = number_of_accounts;
    }



}
